#include <klib.h>

#define MAX_WIDTH 40
#define MAX_HEIGHT 40

enum map_ele{
  empty = 0, apple = 1,
  wall = 2,  bodyu = 3,
  bodyr = 4, bodyd = 5,
  bodyl = 6 
};

static uint32_t snake_map[MAX_HEIGHT][MAX_WIDTH];
static uint32_t empty_cnt;

struct pnt{
  uint32_t x,y;
}head, tail;
typedef struct pnt pnt;

uint32_t w, h;

static int32_t score;

static void gen_apple(){
  uint32_t index = (rand() % empty_cnt) + 1;
  for(uint32_t i = 0; i < MAX_HEIGHT; i ++)
    for(uint32_t j = 0; j < MAX_WIDTH; j ++){
      if(snake_map[i][j] == empty)
        index --;
      if(index == 0){
        snake_map[i][j] = apple;
        empty_cnt --;
        return;
      }
    } 
}

static void init_game(){
  for(uint32_t i = 0; i < MAX_HEIGHT; i ++){
    for(uint32_t j = 0; j < MAX_WIDTH; j ++){
      if(i == 0 || i == MAX_HEIGHT - 1
          || j == 0 || j == MAX_WIDTH - 1)
        snake_map[i][j] = wall;
      else
        snake_map[i][j] = empty;
    }
  }

  head.x = (MAX_WIDTH + 1)/2;
  head.y = (MAX_HEIGHT)/2;
  tail.x = (MAX_WIDTH - 1)/2;
  tail.y = (MAX_HEIGHT)/2;

  snake_map[head.y][head.x] = bodyr;
  snake_map[tail.y][tail.x] = bodyr;

  score = 0;
  empty_cnt = 0;
  for(uint32_t i = 0; i < MAX_HEIGHT; i ++){
    for(uint32_t j = 0; j < MAX_WIDTH; j ++){
      if (snake_map[i][j] == empty)
        empty_cnt ++;
    }
  }

  gen_apple();
}

#define _head snake_map[head.y][head.x]
#define _tail snake_map[tail.y][tail.x]

static uint32_t step_game_do(){
  int32_t x[]={0,1,0,-1};
  int32_t y[]={-1,0,1,0};

  int32_t dx = x[_head - bodyu];
  int32_t dy = y[_head - bodyu];

  int32_t tx = x[_tail - bodyu];
  int32_t ty = y[_tail - bodyu];

#define _next snake_map[head.y + dy][head.x + dx]

  if(_next != empty && _next != apple){
    puts("Game over! \n");
    return 0;
  }
  else if(_next == apple){
    gen_apple();
    _next = _head;
    head.x += dx;
    head.y += dy;
    score ++;
  }
  else{
    _next = _head;
    head.x += dx;
    head.y += dy;
    _tail = empty;
    tail.x += tx;
    tail.y += ty;
  }
  return 1;

#undef _next 
}

uint32_t col[]={
  0xffffff, 0xff0000, 0x000000, 0x3f9fff,
  0x3f9fff, 0x3f9fff, 0x3f9fff, 0x1f6fff
};

static uint32_t pixels[2048];

static char font8x8_basic[128][8];

static void print_char(uint8_t ch, uint32_t x, uint32_t y, uint32_t size){
  for(uint32_t i = 0; i < 8 * size; i ++){
    uint8_t _tmp = 1;
    for(uint32_t j = 0; j < 8 * size; j ++){
      if(font8x8_basic[(uint32_t)ch][i / size] & _tmp)
        pixels[i * 8 * size + j] = 0xffffff;
      else
        pixels[i * 8 * size + j] = 0x000000;
      if(j % size == 0)
        _tmp <<= 1;
    }
  }
  draw_rect(pixels, x, y, 8 * size, 8 * size);
}
 
static char str[128] = " SCORE";
static char num[128];
const uint32_t FONT_SIZE = 2;

static void print_string(char s[], uint32_t x, uint32_t y, uint32_t size){
  uint32_t len = strlen(s);
  for(uint32_t i = 0; i < len; i ++)
    print_char(s[i], x + i * 8 * size , y, size);
}

#define CLEAR_SCREEN() \
  { \
    uint32_t _SIDE = (w / MAX_WIDTH < h / MAX_HEIGHT) \
      ? w / MAX_WIDTH : h / MAX_HEIGHT; \
    for(uint32_t x = 0; x < w / _SIDE; x ++){ \
      for(uint32_t y = 0; y < h / _SIDE; y ++){ \
        for(uint32_t i = 0; i < _SIDE * _SIDE; i++) \
          pixels[i] = 0x000000; \
        draw_rect(pixels, x * _SIDE, y * _SIDE, _SIDE, _SIDE); \
      } \
    } \
  }

static void show_welcome(){ 
  CLEAR_SCREEN();

  print_string("WELCOME", w / 2 - 96, h / 2 - 64, 4);
  print_string("SNAKE", w / 2 - 64, h / 2 - 32, 4);
}

static void show_gameover(){
  CLEAR_SCREEN(); 
  print_string("GAME OVER", w / 2 - 128, h / 2 - 32, 4);
}

static void show(){
  uint32_t _SIDE = (w / MAX_WIDTH < h / MAX_HEIGHT)
    ? w / MAX_WIDTH : h / MAX_HEIGHT;

  for(uint32_t x = 0; x < MAX_WIDTH; x ++){
    for(uint32_t y = 0; y < MAX_HEIGHT; y ++){
      for(uint32_t i = 0; i < _SIDE * _SIDE; i++)
        pixels[i] = col[snake_map[y][x]];
      draw_rect(pixels, x * _SIDE, y * _SIDE, _SIDE, _SIDE);
    }
  }

  for(uint32_t i = 0; i < _SIDE * _SIDE; i++)
    pixels[i] = col[7];
  draw_rect(pixels, head.x * _SIDE, head.y * _SIDE, _SIDE, _SIDE);

  sprintf(num, " %05d", score);
  print_string(str, (MAX_WIDTH+2)*_SIDE, (8*FONT_SIZE)*2, FONT_SIZE);
  print_string(num, (MAX_WIDTH+2)*_SIDE, (8*FONT_SIZE)*3, FONT_SIZE);
}

static uint32_t step_game(){
  uint32_t ret = 1;
  _DEV_INPUT_KBD_t event = { .keycode = _KEY_NONE };
#define KEYNAME(key) \
  [_KEY_##key] = #key,
  _io_read(_DEV_INPUT, _DEVREG_INPUT_KBD, &event, sizeof(event));


  if (event.keycode != _KEY_NONE && event.keydown) {
    switch(event.keycode){
      case _KEY_W: if(_head != bodyd) {_head = bodyu;} break;
      case _KEY_D: if(_head != bodyl) {_head = bodyr;} break;
      case _KEY_S: if(_head != bodyu) {_head = bodyd;} break;
      case _KEY_A: if(_head != bodyr) {_head = bodyl;} break;
      case _KEY_Q: return 0; break;
      default: break;
    }
  }
  
  ret = step_game_do();
  show();

  return ret;
}

